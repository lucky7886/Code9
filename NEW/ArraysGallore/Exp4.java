package ArraysGallore;

public class Exp4 {

	public static void main(String[] args) {

		boolean i;

		while (i = true)
			System.out.println("i");
		int x = 7;
		switch (x) {
		case 2:
			System.out.println("2");
		default:
			System.out.println("default");
			break;
		case 7:
			System.out.println("3");
		case 4:
			System.out.println("4");
		}

		char y = 'y';
		switch (y) {
		case 'l':
			System.out.println("2");
		default:
			System.out.println("default");
			break;
		case 'y':
			System.out.println("3");
		case 'g':
			System.out.println("4");
		}

	}

}
