package ArraysGallore;

public class Random_1 {

	public static void main(String[] args) {
		int height=6;
		



		for(int i=0;i<height;i++)

		{
		    

		for(int j=0;j<height;j++)

		{
		      

		if(i>0 && i < height-1 && j>0 && j< height-1)

		{
		       

		 
		System.out.print(" ");
		      


		}

		else

		{
		       
		 
		System.out.print("*");
		     
		 
		}
		    

		}
		    

		System.out.println("");
		 

		 
		}
	}

}
