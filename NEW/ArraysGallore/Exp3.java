package ArraysGallore;

public class Exp3 {

	public static void main(String[] args) {

		boolean a;

		if (a = false)
			System.out.println("Hello");
		int x = 5;
		switch (x) {
		case 1:
			System.out.println("Hello1");
			break;
		case 2:
			System.out.println("Hello2");
			break;
		case 5:
			System.out.println("Hello3");
			break;
		case 4:
			System.out.println("Hello4");
			break;
		default:
			System.out.println("Hello5");

		}

		String g = "hello";
		switch (g) {
		case "":
			System.out.println("Hello1");
			break;
		case " ":
			System.out.println("Hello2");
			break;
		case "  ":
			System.out.println("Hello3");
			break;
		case "hello":
			System.out.println("Hello4");
			break;
		default:
			System.out.println("Hello5");

		}

		char h = 'j';
		switch (h) {
		case 'q':
			System.out.println("Hello1");
			break;
		case 'w':
			System.out.println("Hello2");
			break;
		case 'g':
			System.out.println("Hello3");
			break;
		case 'j':
			System.out.println("Hello4");
			break;
		default:
			System.out.println("Hello5");

		}

		short b = 129;
		int k = b;

	}

}
